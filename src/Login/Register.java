    public boolean isEmpty() {
        if (usernameField.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Username is missing");
            return false;
        } else if (String.valueOf(passwordField.getPassword()).isEmpty()) {
            JOptionPane.showMessageDialog(this, "Password is missing");
            return false;
        } else if (String.valueOf(verPasswordField.getPassword()).isEmpty()) {
            JOptionPane.showMessageDialog(this, "Verification password is missing");
            return false;
        }
        return true;
    }

