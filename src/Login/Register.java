    private void RegisterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterButtonActionPerformed
        if (isEmpty()) {
            String username = usernameField.getText();
            String password = String.valueOf(passwordField.getPassword());
            String verPassword = String.valueOf(verPasswordField.getPassword());
            if (!verPassword.equals(password)) {
                JOptionPane.showMessageDialog(this, "Passwords are not the same");
                return;
            }

            Client client = new Client(username);
            if (client.userExists(username)) {
                JOptionPane.showMessageDialog(this, "Username already used");
            } else {
                boolean successful = client.addUser(username, password);
                if (successful) {
                    UserHome uh = new UserHome(username, client);
                    uh.setVisible(true);
                    uh.pack();
                    this.dispose();
                }
            }
          client.close();
        }
    }//GEN-LAST:event_RegisterButtonActionPerformed

